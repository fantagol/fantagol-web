      coalesce((d.fantacalcio_rules ->> 'attack_cantonata_multiplier')::numeric, 2),
    'attack_surprise_multiplier',
      coalesce((d.fantacalcio_rules ->> 'attack_surprise_multiplier')::numeric, 2),
