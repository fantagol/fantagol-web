-- ============================================================================
-- FANTAGOL
-- Migration 041: Fantacalcio Preview Builder
--
-- Scope:
--   - freeze Fantacalcio Goals Profile v1 (threshold 23, constant step 10);
--   - version active League Scoring Profiles to schema_version 2;
--   - deterministic Fantacalcio Preview composition from an immutable
--     Points Preview Round Simulation;
--   - Attack / Defense strategic transformation from submitted snapshots;
--   - conversion of Fantacalcio points into goals;
--   - fixture preview generation without duplicating component-level scoring;
--   - authenticated member read RPC for the latest Fantacalcio Preview.
--
-- Out of scope:
--   - Prediction Resolution recalculation;
--   - One-to-One Preview;
--   - standings preview and ranking ledger;
--   - realtime publication and Live State orchestration;
--   - certification commit.
--
-- Architectural rule:
--   Each Preview Builder stores only the new knowledge generated by its own
--   transformation. Prediction components remain authoritative in the Points
--   Preview and are not duplicated in the Fantacalcio Preview.
-- ============================================================================

begin;

