-- 4. AUTHENTICATED MEMBER READ RPC
-- ============================================================================

create or replace function public.get_my_fantacalcio_preview_rpc(
  p_league_round_id uuid
)
returns table (
  simulation_id uuid,
  simulation_version integer,
  simulation_status text,
  simulation_hash text,
  manifest jsonb,
  round_view jsonb,
  matches jsonb,
  fixture jsonb
)
language plpgsql
stable
security definer
set search_path to public, pg_temp
as $function$
declare
  v_user_id uuid;
  v_member_id uuid;
  v_simulation public.latest_round_simulation_v%rowtype;
begin
  v_user_id := auth.uid();

  if v_user_id is null then
    raise exception using
      errcode = 'P0001',
      message = 'AUTHENTICATION_REQUIRED';
  end if;

  select lm.id
  into v_member_id
  from public.league_rounds lr
  join public.league_members lm
    on lm.league_id = lr.league_id
  where lr.id = p_league_round_id
    and lm.user_id = v_user_id
    and lm.status = 'active'
  limit 1;

  if v_member_id is null then
    raise exception using
      errcode = 'P0001',
      message = 'ACTIVE_LEAGUE_MEMBERSHIP_REQUIRED';
  end if;

  select lrs.*
  into v_simulation
  from public.latest_round_simulation_v lrs
  where lrs.league_round_id = p_league_round_id
    and lrs.digital_twin ? 'fantacalcio_preview'
  limit 1;

  if not found then
    return;
  end if;

  return query
  select
    v_simulation.simulation_id,
    v_simulation.simulation_version,
    v_simulation.status,
    v_simulation.simulation_hash,
    v_simulation.digital_twin -> 'manifest',
    v_simulation.digital_twin -> 'round',
    v_simulation.digital_twin -> 'matches',
    coalesce(
      (
        select x
        from jsonb_array_elements(
          v_simulation.digital_twin #> '{fantacalcio_preview,fixtures}'
        ) x
        where (x #>> '{home,member_id}')::uuid = v_member_id
           or nullif(x #>> '{away,member_id}', '')::uuid = v_member_id
        limit 1
      ),
      '{}'::jsonb
    );
end;
$function$;

